func transposeArray(originalArray: [[String]]) -> [[String]] {
    var transposedArray = originalArray
    
    for rowIndex in 0..<originalArray.count {
        for characterIndex in 0..<originalArray[rowIndex].count {
            let character = originalArray[rowIndex][characterIndex]
            transposedArray[characterIndex][rowIndex] = character
        }
    }
    
    return transposedArray
